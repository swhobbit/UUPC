void main( int argc, char **argv )
{

/*--------------------------------------------------------------------*/
/*                       Announce our parentage                       */
/*--------------------------------------------------------------------*/

   banner( argv );

/*--------------------------------------------------------------------*/
/*         Determine if UUPC/extended may have been installed         */
/*--------------------------------------------------------------------*/

   if (getenv(UUPCSYSRC) == NULL )
      installed = KWFalse;

   if (installed)
      update();
   else
      install();

} /* main */

